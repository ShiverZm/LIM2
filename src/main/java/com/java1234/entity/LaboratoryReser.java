package com.java1234.entity;


public class LaboratoryReser {
	
    private Integer id;
    private String labName;
    private String labResPerson;
    private String labResTime;
    
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getLabName() {
		return labName;
	}
	public void setLabName(String labName) {
		this.labName = labName;
	}
	public String getLabResPerson() {
		return labResPerson;
	}
	public void setLabResPerson(String labResPerson) {
		this.labResPerson = labResPerson;
	}
	public String getLabResTime() {
		return labResTime;
	}
	public void setLabResTime(String labResTime) {
		this.labResTime = labResTime;
	}
}
